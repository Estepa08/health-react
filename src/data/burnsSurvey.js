// Опционально: функции для подсчета результатов
export const calculateBurnsScore = (answers) => {
  // answers: объект вида { "1": 2, "2": 0, ... } где значение - индекс выбранного ответа (0-4)
  const total = Object.values(answers).reduce((sum, score) => sum + score, 0)

  let level = ''
  let description = ''

  if (total <= 5) {
    level = 'Депрессия отсутствует'
    description = 'Вы чувствуете себя хорошо. Поздравляем!'
  } else if (total <= 10) {
    level = 'Нормальное, но несчастливое состояние'
    description = 'Вы можете быть не совсем довольны жизнью, но это не депрессия.'
  } else if (total <= 25) {
    level = 'Слабо выраженная депрессия'
    description = 'Рекомендуется обратить внимание на свое состояние.'
  } else if (total <= 50) {
    level = 'Умеренная депрессия'
    description = 'Возможно, вам стоит обратиться к специалисту.'
  } else if (total <= 75) {
    level = 'Сильно выраженная депрессия'
    description = 'Рекомендуется обратиться за профессиональной помощью.'
  } else {
    level = 'Крайняя степень депрессии'
    description = 'Настоятельно рекомендуется немедленно обратиться к специалисту.'
  }

  return { total, level, description }
}

